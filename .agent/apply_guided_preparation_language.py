from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]


def replace_once(relative_path: str, old: str, new: str) -> None:
    path = ROOT / relative_path
    source = path.read_text(encoding='utf-8')
    count = source.count(old)
    if count != 1:
        raise SystemExit(f'{relative_path}: ожидался один фрагмент, найдено {count}: {old[:80]}')
    path.write_text(source.replace(old, new, 1), encoding='utf-8')


def replace_all(relative_path: str, replacements: list[tuple[str, str]]) -> None:
    path = ROOT / relative_path
    source = path.read_text(encoding='utf-8')
    for old, new in replacements:
        if old not in source:
            raise SystemExit(f'{relative_path}: отсутствует фрагмент: {old[:100]}')
        source = source.replace(old, new)
    path.write_text(source, encoding='utf-8')


